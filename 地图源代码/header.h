#define  GET_SHORT(date)    ((((date)&0x00ff)<<8)|((date)>>8))//将两字节转换为短整型
#define  GET_INT(date)      ((GET_SHORT( date & 0x0000ffff))<<16)|(GET_SHORT(date>>16))//将四字节转换为整型
#define  GET_CLASS(date)    (date & 15)//从四字节地图信息中获得dispclass值
#define  GET_CROSS(date)    ((date>>4) & 7)//从四字节地图信息中获得branch值
#define  GET_FLAG(date)     ((date>>7) & 1)//从四字节地图信息中获得flag值
